// Variables in Java

public class Variables {
    public static void main(String args[]) {

        int a = 10;
        float b = 3.14f;
        char c = '$';
        String name = "JavaPro";

        System.out.println(a);
        System.out.println(b);
        System.out.println(name);
        System.out.println(c);
        
    }
}