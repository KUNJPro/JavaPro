// Relational Operators

public class Relational {
    public static void main(String[] args) {
        
        int X = 10;
        int Y = 10;

        System.out.println((X == Y)); 
        System.out.println((X != Y));
        System.out.println((X > Y));
        System.out.println((X < Y));
        System.out.println((X >= Y));
        System.out.println((X <= Y));

    }
}