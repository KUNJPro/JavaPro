// OPERATORS
// TERNARY OPERATOR
// Example : ODD - EVEN

public class Ternary {
    public static void main(String args[]) {

        int Number = 10;

        // Using Ternary Operator
        String type = ((Number % 2) == 0) ? "Even Number" : "Odd Number" ;

        System.out.println(type);

    }
}