// Type Conversion

import java.util.*;

public class TypeConversion {
    public static void main(String args[]) {
        Scanner scn = new Scanner(System.in);

        float number = scn.nextInt();       // float to int is Possible but int to float is not Possible
        System.out.println(number); 

        scn.close();

    }
}

